type I18nKey = keyof typeof STRINGS.en;

export function normalizeLanguageSetting(value: unknown): LanguageSetting {
  return value === "en" || value === "ja" || value === "auto" ? value : "auto";
}

export function resolveLanguage(setting: LanguageSetting | undefined, browserLanguage = ""): ResolvedLanguage {
  if (setting === "en" || setting === "ja") {
    return setting;
  }
  return browserLanguage.toLowerCase().startsWith("ja") ? "ja" : "en";
}

export function t(language: ResolvedLanguage, key: I18nKey, params: Params = {}): string {
  const template = STRINGS[language][key] || STRINGS.en[key] || key;
  return template.replace(/\{([a-zA-Z0-9_]+)\}/g, (_match, name: string) => String(params[name] ?? ""));
}

export function dateLocale(language: ResolvedLanguage): string {
  return language === "ja" ? "ja-JP" : "en-US";
}
